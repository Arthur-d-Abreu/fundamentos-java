package controle;

public class For1 {
	
	public static void main(String[] args) {
	
		
		//Usamos For quando temos um número determinado de repetições
		for(int contador = 1; contador <= 10; contador++) {
			System.out.printf("i = %d\n", contador);
		}
		
	
		
		/*
		 * Laço infinito
		 * for(;;){
		 * 	System.out.println("Fim!")
		 * }
		 */		
	}	
}
