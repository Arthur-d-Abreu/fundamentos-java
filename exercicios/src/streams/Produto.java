package streams;

public class Produto {
	
	final String nome;
	final double preco;
	final double desconto;
	final double frete;
	
	
	public Produto(String produto,double preco, double desconto, double frete) {
		super();
		this.nome = produto;
		this.desconto = desconto;
		this.frete = frete;
		this.preco = preco;
	}
	
	
	
}
